import UIKit

extension UIColor {
    
    static var primaryColor: UIColor {
        return UIColor(red: 25/255, green: 26/255, blue: 119/255, alpha: 1.0)
    }
    
    static var secondaryColor: UIColor {
        return UIColor(red: 235/255, green: 110/255, blue: 45/255, alpha: 1.0)
    }
    
    static var darkTextColor: UIColor {
        return UIColor(red: 20/255, green: 18/255, blue: 60/255, alpha: 1.0)
    }
    
    static var lightBackgroundColor: UIColor {
        return UIColor(red: 238/255, green: 238/255, blue: 238/255, alpha: 1.0)
    }
    
}
